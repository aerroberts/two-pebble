export interface IconProps {
  name: string;
  color?: string;
  className?: string;
}

export function Icon(props: IconProps) {
  const pascalName = props.name
    .split('-')
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join('');

  const LucideIcon = iconMap[pascalName] ?? CircleQuestionMark;

  const classes = `${props.color ?? 'text-content'} ${props.className ?? ''}`.trim();

  return <LucideIcon size={15} className={classes} />;
}
